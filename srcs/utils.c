#include "lem_in.h"

int 	ft_isdigits(char *str)
{
	int i;

	i = 0;
	while(str[i])
	{
		if (str[i] >= 48 && str[i] <= 57)
		{
			i++;
		}
 		else
			return (0);
	}
	return (1);
}
