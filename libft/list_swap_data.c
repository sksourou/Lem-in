#include "libft.h"

void		list_swap_data(t_list *l1, t_list *l2)
{
	void	*tmp;

	tmp = NULL;
	if (l1 && l2)
	{
		tmp = l1->data;
		l1->data = l2->data;
		l2->data = tmp;
	}
}
